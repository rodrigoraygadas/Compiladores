    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        // Para hacerlo interactivo, se va a traer el archivo con un JFileChooser
        // Ayuda a compartir la info con otras clases
        JFileChooser directorio=new JFileChooser();     //Selector de archivo
        directorio.setFileSelectionMode(JFileChooser.FILES_AND_DIRECTORIES);
        int seleleccion=directorio.showSaveDialog(jPanel1);
        if(seleleccion==JFileChooser.APPROVE_OPTION){
            try{
                File archivo=directorio.getSelectedFile();
                //Clase que trabaje con el archivo
                this.archivo = new Archivo(archivo);
                this.archivo.cargar();
                JOptionPane.showMessageDialog(this, archivo.getAbsolutePath()); //Mostrar mensaje con la ruta
            }catch(Exception ex){
                JOptionPane.showConfirmDialog(rootPane, "Error al guardar los datos");
                
            }
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

