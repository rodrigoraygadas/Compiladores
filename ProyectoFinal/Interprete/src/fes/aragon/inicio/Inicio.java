    private void refrescar(){
        long inicio;
        long transcurrido;
        long espera;
        try{
            inicio = System.nanoTime(); // toma el tiempo actual
            this.jPanel1.repaint(); // Indica que pinte
            transcurrido = System.nanoTime(); // indica el tiempo transucrrido
            espera = 10000 / fps - (transcurrido - inicio)/100000; // Inidica cuanto va a esperar
            espera = (espera < 0) ? 0 : espera; // Verifica la velocidad
            Thread.sleep(espera); // Temporizador
        }catch(InterruptedException ex){
            ex.printStackTrace();
        }
    }
    

